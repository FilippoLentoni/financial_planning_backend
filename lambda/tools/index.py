def _json_safe(value: Any) -> Any:
    if isinstance(value, Decimal):
        return float(value)
    if isinstance(value, dict):
        return {key: _json_safe(item) for key, item in value.items()}
    if isinstance(value, list):
        return [_json_safe(item) for item in value]
    return value


def _stable_id(prefix: str, payload: dict[str, Any]) -> str:
    raw = json.dumps(payload, sort_keys=True, default=str)
    digest = hashlib.sha256(raw.encode("utf-8")).hexdigest()[:12]
    return f"{prefix}-{digest}"


def _put_state(item: dict[str, Any]) -> None:
    MEMORY_STATE[str(item["id"])] = _json_safe(item)
    table = _table()
    if table is None:
        return
    ttl = int(time.time()) + 30 * 24 * 60 * 60
    table.put_item(Item=json.loads(json.dumps({**item, "ttl": ttl, "updatedAt": int(time.time())}), parse_float=Decimal))


def _get_state(item_id: str) -> dict[str, Any] | None:
    if item_id in MEMORY_STATE:
        return _json_safe(MEMORY_STATE[item_id])
    table = _table()
    if table is None:
        return None
    try:
        result = table.get_item(Key={"id": item_id})
    except ClientError:
        return None
    item = result.get("Item")
    return _json_safe(item) if item else None


def _scan_state(item_type: str | None = None, limit: int = 25) -> list[dict[str, Any]]:
    memory_items = list(MEMORY_STATE.values())
    if item_type:
        memory_items = [item for item in memory_items if item.get("type") == item_type]

    table = _table()
    items = memory_items
    if table is not None:
        try:
            scan_args: dict[str, Any] = {}
            if item_type:
                scan_args.update(
                    {
                        "FilterExpression": "#type = :type",
                        "ExpressionAttributeNames": {"#type": "type"},
                        "ExpressionAttributeValues": {":type": item_type},
                    }
                )
            result = table.scan(**scan_args)
            items = result.get("Items", [])
        except ClientError:
            items = memory_items

    normalized = [_json_safe(item) for item in items]
    normalized.sort(key=lambda item: int(item.get("createdAt") or 0), reverse=True)
    return normalized[:limit]


