def _require_model_input(input_id: str) -> dict[str, Any]:
    if not input_id:
        raise ValueError("input_id is required")
    state = _get_state(input_id)
    if state and state.get("type") == "model-input":
        return state
    if input_id in {"demo-model-input", "default"} or input_id == _default_model_input()["id"]:
        return _default_model_input()
    raise ValueError(f"model input not found: {input_id}")


def _require_model_run(run_id: str) -> dict[str, Any]:
    if not run_id:
        raise ValueError("run_id is required")
    state = _get_state(run_id)
    if state and state.get("type") == "portfolio-optimization":
        return state
    if run_id.startswith("portfolio-plan-"):
        return _require_simulation({"simulation_id": run_id})
    raise ValueError(f"model run not found: {run_id}")


def _latest_run_for_input(input_id: str) -> dict[str, Any] | None:
    if not input_id:
        return None
    runs = [
        run
        for run in _scan_state("portfolio-optimization", 100)
        if str(run.get("inputId") or "") == input_id
    ]
    return runs[0] if runs else None


def _resolve_model_run_or_input(args: dict[str, Any]) -> tuple[dict[str, Any], str]:
    run_id = str(args.get("run_id") or args.get("simulation_id") or "").strip()
    input_id = str(args.get("input_id") or "").strip()
    if (run_id.startswith("model-input-") or run_id in {"demo-model-input", "default"}) and not input_id:
        input_id = run_id
        run_id = ""
    if run_id:
        run = _require_model_run(run_id)
        return run, str(run.get("inputId") or "")
    if input_id:
        _require_model_input(input_id)
        run = _latest_run_for_input(input_id)
        if run:
            return run, input_id
        raise ValueError(f"no model output found for input_id: {input_id}; call trigger_math_model first")
    raise ValueError("run_id or input_id is required")


