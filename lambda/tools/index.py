def _table():
    name = os.environ.get("STATE_TABLE_NAME")
    if not name or boto3 is None:
        return None
    return boto3.resource("dynamodb").Table(name)


def _response(status_code: int, body: dict[str, Any]) -> dict[str, Any]:
    return {
        "statusCode": status_code,
        "headers": {
            "content-type": "application/json",
            "access-control-allow-origin": "*",
            "access-control-allow-methods": "GET,POST,OPTIONS",
            "access-control-allow-headers": "Content-Type,Authorization,X-Amz-Date,X-Amz-Security-Token,X-Amz-Content-Sha256",
        },
        "body": json.dumps(_json_safe(body)),
    }


