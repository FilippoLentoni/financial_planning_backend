def list_model_runs(args: dict[str, Any] | None = None) -> dict[str, Any]:
    args = args or {}
    limit = int(args.get("limit") or 10)
    input_limit = int(args.get("inputLimit") or args.get("input_limit") or 50)
    runs = _scan_state("portfolio-optimization", limit)
    model_inputs = _scan_state("model-input", input_limit)
    if not runs:
        seed = create_weekly_model_run({"source": "bootstrap-synthetic-seed"})
        runs = [_get_state(seed["run_id"]) or {}]

    items = []
    runs_by_input: dict[str, list[dict[str, Any]]] = {}
    for run in runs:
        plan = run.get("plan") or run.get("modelOutput", {}).get("plan", {})
        inputs = run.get("inputs", {})
        input_id = str(run.get("inputId") or "")
        model_input = _get_state(input_id) if input_id else None
        input_payload = model_input.get("payload", {}) if model_input else {}
        items.append(
            {
                "run_id": run.get("id"),
                "input_id": input_id,
                "status": run.get("status", "UNKNOWN"),
                "outputReady": run.get("status") == "COMPLETED" and bool(run.get("modelOutput")),
                "createdAt": run.get("createdAt"),
                "createdAtIso": run.get("createdAtIso") or _iso_time(run.get("createdAt")),
                "completedAt": run.get("completedAt"),
                "completedAtIso": run.get("completedAtIso"),
                "portfolioId": inputs.get("portfolioId") or input_payload.get("portfolioId"),
                "riskTarget": inputs.get("riskTarget") or input_payload.get("riskTarget"),
                "asOfDate": inputs.get("asOfDate") or input_payload.get("asOfDate"),
                "source": inputs.get("source") or input_payload.get("source"),
                "cashAvailable": inputs.get("cashAvailable") or input_payload.get("cashAvailable"),
                "expectedValueAtWeek16": plan.get("expectedValueAtWeek16"),
                "expectedReturnPct16w": plan.get("expectedReturnPct16w"),
                "solverStatus": run.get("modelOutput", {}).get("solverStatus"),
                "modelUsed": run.get("modelUsed") or _model_metadata(),
                "description": (
                    f"{inputs.get('riskTarget') or input_payload.get('riskTarget', 'moderate')} plan for "
                    f"{inputs.get('portfolioId') or input_payload.get('portfolioId', 'demo-growth-income')}"
                ),
            }
        )
        if input_id:
            runs_by_input.setdefault(input_id, []).append(items[-1])

    input_items = []
    for model_input in model_inputs:
        input_id = str(model_input.get("id") or "")
        payload = model_input.get("payload", {}) if isinstance(model_input.get("payload"), dict) else {}
        snapshot = payload.get("snapshot", {}) if isinstance(payload.get("snapshot"), dict) else {}
        linked_runs = runs_by_input.get(input_id, [])
        input_items.append(
            {
                "input_id": input_id,
                "createdAt": model_input.get("createdAt"),
                "createdAtIso": model_input.get("createdAtIso") or _iso_time(model_input.get("createdAt")),
                "portfolioId": payload.get("portfolioId"),
                "riskTarget": payload.get("riskTarget"),
                "asOfDate": payload.get("asOfDate"),
                "source": payload.get("source"),
                "cashAvailable": payload.get("cashAvailable"),
                "totalValue": snapshot.get("totalValue"),
                "latest_run_id": linked_runs[0]["run_id"] if linked_runs else None,
                "latest_run_status": linked_runs[0].get("status") if linked_runs else None,
                "latest_run_output_ready": linked_runs[0].get("outputReady") if linked_runs else False,
                "run_ids": [linked_run["run_id"] for linked_run in linked_runs if linked_run.get("run_id")],
                "run_count": len(linked_runs),
            }
        )

    return {
        "items": items,
        "inputs": input_items,
        "latest": items[0] if items else None,
        "count": len(items),
        "inputCount": len(input_items),
        "storage": {
            "table": os.environ.get("STATE_TABLE_NAME") or "in-memory-local",
            "recordTypes": [
                "market-data",
                "market-data-batch",
                "model-input",
                "portfolio-optimization",
                "model-input-override",
            ],
        },
    }


def _handle_http(event: dict[str, Any]) -> dict[str, Any]:
    method = str(event.get("httpMethod") or event.get("requestContext", {}).get("http", {}).get("method") or "GET").upper()
    path = str(event.get("path") or event.get("rawPath") or "")
    if method == "OPTIONS":
        return _response(200, {})
    if path.endswith("/planning/runs") and method == "GET":
        query = event.get("queryStringParameters") or {}
        return _response(200, list_model_runs(query))
    if path.endswith("/planning/runs") and method == "POST":
        raw_body = event.get("body") or "{}"
        body = json.loads(raw_body) if isinstance(raw_body, str) and raw_body.strip() else {}
        if body.get("input_id"):
            return _response(200, trigger_math_model({"input_id": body["input_id"]}))
        if body.get("source") == "yfinance-market-data":
            return _response(200, create_market_data_model_run(body))
        return _response(200, create_weekly_model_run(body))
    return _response(404, {"error": f"unknown route: {method} {path}"})


TOOL_HANDLERS = {
    "healthCheck": lambda args: health_check(),
    "listGateways": lambda args: list_gateways(),
    "listTools": lambda args: list_tools(args.get("gateway")),
    "get_math_model_input": get_math_model_input,
    "get_math_model_output": get_math_model_output,
    "override_math_model_input": override_math_model_input,
    "get_math_model_formulation": get_math_model_formulation,
    "trigger_math_model": trigger_math_model,
    "get_math_model_status": get_math_model_status,
    "run_math_model": run_math_model,
    "override_math_model": override_math_model,
    "listModelRuns": lambda args: list_model_runs(args),
    "createWeeklyModelRun": lambda args: create_weekly_model_run(args),
    "fetchMarketData": lambda args: fetch_market_data(args),
    "createMarketDataModelRun": lambda args: create_market_data_model_run(args),
}


def _normalize_tool_name(tool_name: str) -> str:
    name = str(tool_name or "").strip()
    if name in TOOL_HANDLERS:
        return name
    for delimiter in ("___", "__"):
        if delimiter in name:
            candidate = name.split(delimiter, 1)[1]
            if candidate in TOOL_HANDLERS:
                return candidate
    for known in TOOL_HANDLERS:
        if name.endswith(known):
            return known
    return name


def invoke_tool(tool_name: str, args: dict[str, Any]) -> dict[str, Any]:
    normalized = _normalize_tool_name(tool_name)
    handler = TOOL_HANDLERS.get(normalized)
    if handler is None:
        raise ValueError(f"unknown tool: {tool_name}")
    return handler(args)


def handler(event: dict[str, Any], context: Any) -> dict[str, Any]:
    try:
        if event.get("source") == "financial-planning.weekly-data-pipeline":
            return _json_safe(create_weekly_model_run(event.get("detail") or event))

        if event.get("source") == "financial-planning.market-data-pipeline":
            return _json_safe(create_market_data_model_run(event.get("detail") or event))

        if event.get("httpMethod") or event.get("rawPath") or event.get("requestContext", {}).get("http"):
            return _handle_http(event)

        gateway_tool_name = _agentcore_context_tool_name(context)
        if gateway_tool_name:
            return _json_safe(invoke_tool(gateway_tool_name, event))

        tool_name = event.get("tool") or event.get("name")
        args = event.get("arguments") or event.get("args") or {}
        if not tool_name:
            return _response(400, {"error": "tool is required"})
        return _response(200, invoke_tool(str(tool_name), args))
    except Exception as exc:
        if _agentcore_context_tool_name(context):
            return {"error": str(exc)}
        return _response(400, {"error": str(exc)})
